public class Image {
    private String title;
    private int width;
    private int height;
    private String link;
    private String url;

    public String getTitle() {
        return title;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public String getLink() {
        return link;
    }

    public String getUrl() {
        return url;
    }
}
